package org.firstinspires.ftc.teamcode.Utils;

public class Point {

    public double x, y, theta;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Point(double x, double y, double theta) {
        this.x = x;
        this.y = y;
        this.theta = theta;
    }
}
